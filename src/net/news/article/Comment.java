package net.news.article;

public class Comment {
	private int cid;
	private int aid;
	private String uid;
	private String contents;
	private String lasttime;
	private int comment;
	private int indent;
	public int getCid() {
		return cid;
	}
	public int getAid() {
		return aid;
	}
	public String getUid() {
		return uid;
	}
	public String getContents() {
		return contents;
	}
	public String getLasttime() {
		return lasttime;
	}
	public int getComment() {
		return comment;
	}
	public int getIndent() {
		return indent;
	}
}


